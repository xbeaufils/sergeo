package fr.firesoft.fireTime.genetic;

/**
 * @author xavier
 *
 */
public class NotEnoughEffectifException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6050844192882110707L;

	/**
	 * 
	 */
	public NotEnoughEffectifException() {
	}

	/**
	 * @param message
	 */
	public NotEnoughEffectifException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public NotEnoughEffectifException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public NotEnoughEffectifException(String message, Throwable cause) {
		super(message, cause);
	}

}
